    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $about = About::findOrFail($id);
        return view('admin.about.show', compact('designation','about'));
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $about = About::findOrFail($id);
        return view('admin.about.edit',compact('about'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $about = About::findOrFail($id);
        $this->validate($request, [
            'description' => 'required',
        ]);
        
        $about->description = $request->get('description');
        $about->facebook_link = $request->get('facebook');
        $about->twitter_link = $request->get('twitter');
        $about->rss_link = $request->get('rss');
        $about->save();
        return redirect('/about')->with('message', 'Designation Update successful!');
    }

